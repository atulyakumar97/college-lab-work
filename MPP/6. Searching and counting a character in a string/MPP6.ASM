DATA SEGMENT
ORG 200H
CHAR_FLAG DB 00H
CHAR_COUNT DB 00H
DATA ENDS

EXTRA SEGMENT
ORG 300H
STRING DB 'ABCABCABC'
EXTRA ENDS

CODE SEGMENT
ASSUME CS: CODE, DS: DATA, ES: EXTRA
START:  MOV AX,DATA
        MOV DS,AX
        MOV AX,EXTRA
        MOV ES,AX
        LEA DI,STRING
        MOV CL,09
        MOV AL,'A'
UP:     SCASB
        JNZ DOWN
        MOV CHAR_FLAG, 1H
        INC CHAR_COUNT
DOWN:   DEC CL
        JNZ UP
        INT 3


CODE ENDS
END START
